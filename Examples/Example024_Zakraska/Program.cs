void PrintImage(int[,] image)
{
    for (int i = 0; i < image.GetLength(0); i++) // номер строки 0 - количество строк
        {
            for (int j = 0; j < image.GetLength(1); j++) // номер столбца 
            {
                //Console.Write($"{image[i, j]} ");
                if(image[i,j] == 0) Console.Write($" ");
                else Console.Write($"+");
            }
            Console.WriteLine();
        }
}

void FllImage(int row, int col)
{
    if (pic[row, col] == 0)
    {
        pic[row, col] = 1;
        FllImage(row-1, col);
        FllImage(row, col-1);
        FllImage(row+1, col);
        FllImage(row, col+1);
    }
}

